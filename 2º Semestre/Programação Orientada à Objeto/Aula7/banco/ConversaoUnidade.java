class ConversaoUnidade
{
    public static double pol2cm(double pol)
    {
        double cm = pol * 2.54;
        return cm;
    }
    public static double pes2cm(double pes)
    {
        double cm = pes * 30.48;
        return cm;
    }
    public static double milha2km(double milha)
    {
        double km = milha * 1.609;
        return km;
    }
}
