public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int x = 15;
        int y = 10;
        int z = 5;
        int add= (x+y+z);
        int sub= (x-y-z);
        int mult= (x*y)*z;
        float div= (x+y)/z;
        System.out.println("Adição: "+ add);
        System.out.println("Subtração: "+ sub);
        System.out.println("Multiplicação: "+ mult);
        System.out.println("Divisão: "+ div);
    }
}
